/// Pure decision engine for position management
///
/// The Engine processes positions and market data to decide actions.
/// It is completely deterministic and performs no I/O.
///
/// # Responsibilities
///
/// 1. **Trailing Stop Updates**: When price moves favorably, update the stop
/// 2. **Exit Triggers**: When price hits trailing stop, trigger exit
/// 3. **Event Generation**: Emit events for all state changes
///
/// # Example
///
/// ```ignore
/// let engine = Engine::new(risk_config);
/// let decision = engine.process_active_position(&position, &market_data)?;
/// for action in decision.actions {
///     executor.execute(action).await?;
/// }
/// ```
#[derive(Debug, Clone)]
pub struct Engine {
    /// Risk configuration
    risk_config: RiskConfig,
}

impl Engine {
    /// Create a new Engine with risk configuration
    pub fn new(risk_config: RiskConfig) -> Self {
        Self { risk_config }
    }

    /// Get the risk configuration
    pub fn risk_config(&self) -> &RiskConfig {
        &self.risk_config
    }

    /// Update the risk configuration (e.g., when operator provides new
    /// capital).
    pub fn update_risk_config(&mut self, risk_config: RiskConfig) {
        self.risk_config = risk_config;
    }

    // =========================================================================
    // Entry Logic
    // =========================================================================

    /// Process detector signal to decide entry
    ///
    /// Called when a DetectorTask fires a signal for an Armed position.
    /// Validates the signal, calculates position size, and returns entry
    /// actions.
    ///
    /// # Arguments
    ///
    /// * `position` - The armed position (must be in Armed state)
    /// * `signal` - The detector signal with entry parameters
    ///
    /// # Returns
    ///
    /// * `Ok(EngineDecision)` with EntryOrderRequested + PlaceEntryOrder
    ///   actions
    /// * `Err(EngineError)` if validation fails
    ///
    /// # State Transition
    ///
    /// ```text
    /// Armed → Armed (Entering only after EntryOrderAccepted)
    /// ```
    pub fn decide_entry(
        &self,
        position: &Position,
        signal: &DetectorSignal,
    ) -> Result<EngineDecision, EngineError> {
        // 1. Validate position is Armed
        if !matches!(position.state, PositionState::Armed) {
            return Err(EngineError::InvalidPositionState {
                expected: "armed".to_string(),
                actual: position.state.name().to_string(),
            });
        }

        // 2. Validate signal matches position
        signal
            .validate_for_position(position)
            .map_err(|e| EngineError::DomainError(e))?;

        // 3. Validate and get tech stop distance
        let tech_stop = signal.tech_stop_distance();
        tech_stop.validate().map_err(|e| EngineError::DomainError(e))?;

        // 4. Resolve the effective stop basis for sizing (ADR-0042). When the
        //    detector supplied an invalidation guard, clamp the chart-derived
        //    technical stop beyond the recent adverse extreme before sizing so
        //    the 1% budget absorbs the worst realizable loss at the guarded
        //    level. The domain clamp helper at zero buffer returns the clamped
        //    level unchanged; `None` guard is the identity (historical sizing).
        let guard = signal
            .technical_stop_analysis
            .as_ref()
            .and_then(|audit| audit.invalidation_guard_level);
        let effective_stop_level = robson_domain::value_objects::effective_stop_price_with_guard(
            position.side,
            tech_stop.initial_stop,
            Decimal::ZERO,
            guard,
        );

        // Calculate position size from the (possibly clamped) effective stop
        // distance. A guard that widens the effective distance past the policy
        // cap makes this return Err — the entry is rejected (guard too wide).
        let quantity = calculate_position_size(
            &self.risk_config,
            &signal.entry_price,
            &tech_stop,
            effective_stop_level,
        )
        .map_err(EngineError::DomainError)?;

        debug!(
            position_id = %position.id,
            signal_id = %signal.signal_id,
            entry_price = %signal.entry_price,
            stop_loss = %signal.stop_loss,
            quantity = %quantity,
            "Entry signal received, requesting entry order"
        );

        // 5. Generate entry order ID and deterministic client_order_id
        let entry_order_id = uuid::Uuid::now_v7();
        let client_order_id = signal.signal_id.to_string();

        // 6. Build actions — no Entering transition until exchange ack
        let actions = vec![
            // Emit signal received event (audit)
            EngineAction::EmitEvent(Event::EntrySignalReceived {
                position_id: position.id,
                signal_id: signal.signal_id,
                entry_price: signal.entry_price,
                stop_loss: signal.stop_loss,
                quantity,
                timestamp: Utc::now(),
            }),
            // Emit governed intent (pre-exchange, cycle_id stamped by GovernedAction)
            EngineAction::EmitEvent(Event::EntryOrderRequested {
                position_id: position.id,
                cycle_id: None,
                order_id: entry_order_id,
                client_order_id: client_order_id.clone(),
                expected_price: signal.entry_price,
                quantity,
                signal_id: signal.signal_id,
                timestamp: Utc::now(),
            }),
            // Place entry order on exchange
            EngineAction::PlaceEntryOrder {
                position_id: position.id,
                cycle_id: None,
                symbol: position.clone().symbol,
                side: position.side.entry_action(),
                quantity,
                order_id: entry_order_id,
                client_order_id,
                expected_price: signal.entry_price,
                signal_id: signal.signal_id,
            },
        ];

        // Position stays Armed — Entering only after EntryOrderAccepted from executor.
        Ok(EngineDecision::with_actions(actions))
    }

    /// Process entry order fill
    ///
    /// Called when the entry market order is filled.
    /// Transitions position to Active state with initial trailing stop.
    ///
    /// # Arguments
    ///
    /// * `position` - The entering position (must be in Entering state)
    /// * `fill_price` - Actual fill price from exchange
    /// * `filled_quantity` - Actual filled quantity
    /// * `fee` - Trading fee paid (from OrderResult)
    /// * `filled_at` - When the fill occurred (from exchange, not local clock)
    /// * `binance_position_id` - Optional Binance USD-M Futures position ID
    /// * `invalidation_guard_level` - Entry-time invalidation guard level
    ///   (ADR-0042), sourced from the detector signal/audit. Carried onto the
    ///   `Active` state and the `EntryFilled` event so the executable stop
    ///   (soft exit + insurance) stays clamped to the recent adverse extreme
    ///   until the first trailing advance releases it.
    ///
    /// # Returns
    ///
    /// * `Ok(EngineDecision)` with updated position in Active state
    /// * `Err(EngineError)` if validation fails
    ///
    /// # State Transition
    ///
    /// ```text
    /// Entering → Active (trailing stop = entry - tech_distance)
    /// ```
    pub fn process_entry_fill(
        &self,
        position: &Position,
        fill_price: Price,
        filled_quantity: Quantity,
        fee: rust_decimal::Decimal,
        filled_at: DateTime<Utc>,
        binance_position_id: Option<String>,
        invalidation_guard_level: Option<Price>,
    ) -> Result<EngineDecision, EngineError> {
        // 1. Validate position is Entering
        let entry_order_id = match &position.state {
            PositionState::Entering {
                entry_order_id,
                expected_entry: _,
                signal_id: _,
            } => *entry_order_id,
            other => {
                return Err(EngineError::InvalidPositionState {
                    expected: "entering".to_string(),
                    actual: other.name().to_string(),
                });
            },
        };

        // 2. Get tech stop distance
        let tech_stop = position.tech_stop_distance.as_ref().ok_or_else(|| {
            EngineError::MissingData("Position missing tech_stop_distance".to_string())
        })?;

        // 3. Initial trailing stop = the technical stop itself (no movement yet) In v3
        //    discrete trailing, the stop only moves after a full span of profit. At
        //    entry, zero spans have been completed, so the stop is the initial tech
        //    stop.
        let initial_trailing_stop = tech_stop.initial_stop;

        debug!(
            position_id = %position.id,
            fill_price = %fill_price,
            filled_quantity = %filled_quantity,
            initial_trailing_stop = %initial_trailing_stop,
            "Entry filled, position now active"
        );

        // 4. Create updated position in Active state
        let mut updated_position = position.clone();
        updated_position.state = PositionState::Active {
            current_price: fill_price,
            trailing_stop: initial_trailing_stop,
            favorable_extreme: fill_price,
            extreme_at: filled_at,
            insurance_stop_id: None, // No insurance stop (Robson manages exits)
            // Entry-time invalidation guard (ADR-0042): clamps the effective
            // stop until the first trailing advance releases it.
            invalidation_guard_level,
            last_emitted_stop: None, // No stop emitted yet
        };
        updated_position.entry_price = Some(fill_price);
        updated_position.quantity = filled_quantity;
        updated_position.fees_paid = fee;
        updated_position.entry_filled_at = Some(filled_at);
        updated_position.updated_at = filled_at;

        // 5. Build actions (emit event)
        let actions = vec![
            EngineAction::EmitEvent(Event::EntryFilled {
                position_id: position.id,
                order_id: entry_order_id,
                fill_price,
                filled_quantity,
                fee,
                initial_stop: initial_trailing_stop,
                invalidation_guard_level,
                binance_position_id,
                timestamp: filled_at,
            }),
            // Place a reduce-only protective stop on the exchange at the initial
            // trailing stop (ADR-0039). Placement failure is tolerated by the
            // executor (audit event) — the software stop remains the primary
            // exit path.
            EngineAction::PlaceInsuranceStop {
                position_id: position.id,
                symbol: position.symbol.clone(),
                side: position.side.exit_action(),
                quantity: filled_quantity,
                // Executable price: technical stop offset by the configured
                // buffer (events keep the technical value), clamped to the
                // entry-time guard while it is still active (ADR-0042).
                stop_price: self
                    .effective_stop(position.side, initial_trailing_stop, invalidation_guard_level),
            },
        ];

        Ok(EngineDecision::with_position(actions, updated_position))
    }

    // =========================================================================
    // Active Position Logic (Exit/Trailing Stop)
    // =========================================================================

    /// Process an active position with current market data
    ///
    /// This is the main entry point for the engine.
    /// It checks if the trailing stop should be updated or if exit should
    /// trigger.
    ///
    /// # Arguments
    ///
    /// * `position` - The active position to process
    /// * `market_data` - Current market data
    ///
    /// # Returns
    ///
    /// * `Ok(EngineDecision)` - Actions to execute
    /// * `Err(EngineError)` - If position is not active or data is invalid
    pub fn process_active_position(
        &self,
        position: &Position,
        market_data: &MarketData,
    ) -> Result<EngineDecision, EngineError> {
        // Validate position is active
        let (_current_price_in_state, trailing_stop, favorable_extreme, last_emitted_stop, guard) =
            match &position.state {
                PositionState::Active {
                    current_price,
                    trailing_stop,
                    favorable_extreme,
                    last_emitted_stop,
                    invalidation_guard_level,
                    ..
                } => (
                    *current_price,
                    *trailing_stop,
                    *favorable_extreme,
                    *last_emitted_stop,
                    *invalidation_guard_level,
                ),
                other => {
                    return Err(EngineError::InvalidPositionState {
                        expected: "active".to_string(),
                        actual: other.name().to_string(),
                    });
                },
            };

        // Validate symbol matches
        if position.symbol != market_data.symbol {
            return Err(EngineError::InvalidMarketData(format!(
                "Symbol mismatch: position={}, market={}",
                position.symbol, market_data.symbol
            )));
        }

        // Get tech stop distance
        let tech_stop = position.tech_stop_distance.as_ref().ok_or_else(|| {
            EngineError::MissingData("Position missing tech_stop_distance".to_string())
        })?;

        let current_price = market_data.current_price;
        let observed_watermark =
            Self::observed_watermark(position.side, current_price, favorable_extreme);

        // Check exit first (higher priority)
        if self.should_exit(position.side, current_price, trailing_stop, guard) {
            debug!(
                position_id = %position.id,
                current_price = %current_price,
                trailing_stop = %trailing_stop,
                "Exit triggered"
            );
            let monitor_tick = self.create_position_monitor_tick_event(
                position,
                current_price,
                trailing_stop,
                observed_watermark,
                market_data.timestamp,
            );
            let mut decision = self.create_exit_decision(position, current_price, trailing_stop);
            decision.actions.insert(0, EngineAction::EmitEvent(monitor_tick));
            return Ok(decision);
        }

        // Check if trailing stop should be updated (discrete step logic)
        if let Some(new_stop) = self.calculate_new_trailing_stop(
            position.side,
            current_price,
            favorable_extreme,
            trailing_stop,
            tech_stop,
        ) {
            // Discrete logic already ensures monotonicity, but double-check
            if self.is_more_favorable_stop(position.side, new_stop, trailing_stop) {
                // Idempotency check: only emit if different from last emitted
                if last_emitted_stop != Some(new_stop) {
                    debug!(
                        position_id = %position.id,
                        current_price = %current_price,
                        old_stop = %trailing_stop,
                        new_stop = %new_stop,
                        "Trailing stop updated"
                    );
                    return Ok(self.create_update_stop_decision(
                        position,
                        trailing_stop,
                        new_stop,
                        current_price,
                        observed_watermark,
                        market_data.timestamp,
                    ));
                }
            }
        }

        let monitor_tick = self.create_position_monitor_tick_event(
            position,
            current_price,
            trailing_stop,
            observed_watermark,
            market_data.timestamp,
        );

        Ok(EngineDecision::with_actions(vec![EngineAction::EmitEvent(monitor_tick)]))
    }

    /// Derive the executable stop from the chart-derived technical stop.
    ///
    /// The technical stop stays the conceptual reference everywhere (trailing
    /// ladder, events, persistence); execution — the soft-exit comparison and
    /// the exchange-side insurance stop — triggers a small operator-configured
    /// buffer beyond it (below for longs, above for shorts). Zero buffer, the
    /// default, is the historical behavior. The buffer is priced into
    /// position sizing (Policy 10).
    fn effective_stop(&self, side: Side, technical_stop: Price, guard: Option<Price>) -> Price {
        robson_domain::value_objects::effective_stop_price_with_guard(
            side,
            technical_stop,
            self.risk_config.stop_buffer_bps(),
            guard,
        )
    }

    /// Check if position should exit (executable stop hit)
    ///
    /// The comparison uses the EXECUTABLE stop (technical stop offset by the
    /// configured buffer, clamped to the entry-time guard while it is still
    /// active), so the software exit and the exchange-side insurance stop
    /// trigger at the same level.
    fn should_exit(
        &self,
        side: Side,
        current_price: Price,
        trailing_stop: Price,
        guard: Option<Price>,
    ) -> bool {
        let effective = self.effective_stop(side, trailing_stop, guard);
        match side {
            // LONG: exit when price drops to or below the executable stop
            Side::Long => current_price.as_decimal() <= effective.as_decimal(),
            // SHORT: exit when price rises to or above the executable stop
            Side::Short => current_price.as_decimal() >= effective.as_decimal(),
        }
    }

    /// Calculate new trailing stop using v3 discrete step (span/palmo) logic
    ///
    /// The stop moves in integer multiples of the span, anchored to entry
    /// price. Returns `Some(new_stop)` only when a FULL span step has been
    /// completed. Returns `None` for partial movements.
    fn calculate_new_trailing_stop(
        &self,
        side: Side,
        current_price: Price,
        favorable_extreme: Price,
        trailing_stop: Price,
        tech_stop: &TechnicalStopDistance,
    ) -> Option<Price> {
        let update = trailing_stop::update_trailing_stop_discrete(
            side,
            current_price,
            favorable_extreme,
            trailing_stop,
            tech_stop.entry_price,
            tech_stop.distance,
        );
        update.map(|u| u.new_stop)
    }

    /// Check if new stop is more favorable than current stop
    fn is_more_favorable_stop(&self, side: Side, new_stop: Price, current_stop: Price) -> bool {
        match side {
            // LONG: higher stop is more favorable
            Side::Long => new_stop.as_decimal() > current_stop.as_decimal(),
            // SHORT: lower stop is more favorable
            Side::Short => new_stop.as_decimal() < current_stop.as_decimal(),
        }
    }

    /// Compute the best favorable price visible at this tick.
    fn observed_watermark(side: Side, current_price: Price, favorable_extreme: Price) -> Price {
        match side {
            Side::Long if current_price.as_decimal() > favorable_extreme.as_decimal() => {
                current_price
            },
            Side::Short if current_price.as_decimal() < favorable_extreme.as_decimal() => {
                current_price
            },
            _ => favorable_extreme,
        }
    }

    /// Distance from the favorable watermark back to the current stop trigger.
    fn span_remaining(side: Side, watermark: Price, current_stop: Price) -> Decimal {
        let distance = match side {
            Side::Long => watermark.as_decimal() - current_stop.as_decimal(),
            Side::Short => current_stop.as_decimal() - watermark.as_decimal(),
        };

        distance.max(Decimal::ZERO)
    }

    fn create_position_monitor_tick_event(
        &self,
        position: &Position,
        price: Price,
        current_stop: Price,
        high_watermark: Price,
        timestamp: DateTime<Utc>,
    ) -> Event {
        Event::PositionMonitorTick {
            position_id: position.id,
            symbol: position.symbol.as_pair(),
            price,
            current_stop,
            high_watermark,
            span_remaining: Self::span_remaining(position.side, high_watermark, current_stop),
            timestamp,
        }
    }

    /// Create decision for exit trigger
    fn create_exit_decision(
        &self,
        position: &Position,
        trigger_price: Price,
        stop_price: Price,
    ) -> EngineDecision {
        let reason = ExitReason::TrailingStop;
        let exit_side = position.side.exit_action();

        // Read the live insurance-stop order id (if any) so the market exit can
        // cancel it first (ADR-0039): a reduce-only stop and a market exit must
        // never coexist on the exchange.
        let insurance_stop_id = match &position.state {
            PositionState::Active { insurance_stop_id, .. } => insurance_stop_id.clone(),
            _ => None,
        };

        let mut actions = vec![
            // 1. Trigger exit event
            EngineAction::TriggerExit {
                position_id: position.id,
                reason,
                trigger_price,
                stop_price,
            },
        ];

        // 2. Cancel the protective stop first, if one is live.
        if let Some(order_id) = insurance_stop_id {
            actions.push(EngineAction::CancelInsuranceStop {
                position_id: position.id,
                symbol: position.symbol.clone(),
                order_id,
            });
        }

        actions.extend([
            // 3. Place exit order
            EngineAction::PlaceExitOrder {
                position_id: position.id,
                cycle_id: None,
                symbol: position.symbol.clone(),
                side: exit_side,
                quantity: position.quantity,
                reason,
            },
            // 4. Emit event
            EngineAction::EmitEvent(Event::ExitTriggered {
                position_id: position.id,
                reason,
                trigger_price,
                stop_price,
                timestamp: Utc::now(),
            }),
        ]);

        EngineDecision::with_actions(actions)
    }

    /// Create decision for trailing stop update
    fn create_update_stop_decision(
        &self,
        position: &Position,
        previous_stop: Price,
        new_stop: Price,
        trigger_price: Price,
        observed_watermark: Price,
        tick_timestamp: DateTime<Utc>,
    ) -> EngineDecision {
        // Read the live insurance-stop order id (if any) to decide replace vs
        // place on this trailing advance (ADR-0039).
        let insurance_stop_id = match &position.state {
            PositionState::Active { insurance_stop_id, .. } => insurance_stop_id.clone(),
            _ => None,
        };

        // Update position state with new trailing stop and favorable extreme
        let mut updated_position = position.clone();
        if let PositionState::Active {
            current_price: _,
            trailing_stop: _,
            favorable_extreme: _,
            extreme_at: _,
            insurance_stop_id,
            invalidation_guard_level: _,
            ..
        } = updated_position.state
        {
            // Monotonicity check: stop only moves in favorable direction
            // (enforced by caller via is_more_favorable_stop check)
            //
            // The invalidation guard is an entry-time constraint and is released
            // on the FIRST trailing advance (ADR-0042): for a short the
            // trailing technical stop only moves down, so a permanent guard
            // would pin the stop at the entry high forever. After release the
            // effective stop is the trailing technical stop + buffer (ADR-0041),
            // so it is cleared to None on every advance (a no-op once cleared).
            updated_position.state = PositionState::Active {
                current_price: trigger_price,
                trailing_stop: new_stop,
                favorable_extreme: trigger_price,
                extreme_at: Utc::now(),
                insurance_stop_id,
                invalidation_guard_level: None,
                last_emitted_stop: Some(new_stop),
            };
            updated_position.updated_at = Utc::now();
        }

        // Move the protective insurance stop to the new trailing level
        // (ADR-0039). Replace when a stop is already live; otherwise place
        // (covers positions opened before this feature, or a prior place that
        // failed). Idempotency derives from the exchange order id; failures are
        // tolerated by the executor.
        let insurance_action = match insurance_stop_id {
            Some(previous_order_id) => EngineAction::ReplaceInsuranceStop {
                position_id: position.id,
                symbol: position.symbol.clone(),
                side: position.side.exit_action(),
                quantity: position.quantity,
                previous_order_id,
                new_stop_price: self.effective_stop(position.side, new_stop, None),
            },
            None => EngineAction::PlaceInsuranceStop {
                position_id: position.id,
                symbol: position.symbol.clone(),
                side: position.side.exit_action(),
                quantity: position.quantity,
                stop_price: self.effective_stop(position.side, new_stop, None),
            },
        };

        let actions = vec![
            // 1. Update trailing stop (in-memory only; no exchange I/O)
            EngineAction::UpdateTrailingStop {
                position_id: position.id,
                previous_stop,
                new_stop,
                trigger_price,
            },
            // 2. Move the protective stop to the new level (ADR-0039).
            insurance_action,
            // 3. Emit event
            EngineAction::EmitEvent(Event::TrailingStopUpdated {
                position_id: position.id,
                previous_stop,
                new_stop,
                trigger_price,
                timestamp: Utc::now(),
            }),
            // 4. Emit per-tick audit evidence with the post-update stop.
            EngineAction::EmitEvent(self.create_position_monitor_tick_event(
                position,
                trigger_price,
                new_stop,
                observed_watermark,
                tick_timestamp,
            )),
        ];

        EngineDecision::with_position(actions, updated_position)
    }
}

