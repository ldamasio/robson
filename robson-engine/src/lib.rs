/// Actions that the Engine decides should be executed
///
/// These are pure data - the execution layer handles actual I/O.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum EngineAction {
    /// Place entry order (market order to open position)
    ///
    /// Emitted by `decide_entry` when detector signal is valid.
    PlaceEntryOrder {
        /// Position entering
        position_id: PositionId,
        /// Query/risk cycle identifier proving governed execution.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        cycle_id: Option<uuid::Uuid>,
        /// Symbol to trade
        symbol: Symbol,
        /// Order side (Long → Buy, Short → Sell)
        side: robson_domain::OrderSide,
        /// Calculated quantity (from position sizing)
        quantity: Quantity,
        /// Local order identifier (generated by engine)
        order_id: robson_domain::OrderId,
        /// Deterministic id sent to exchange (= signal_id.to_string())
        client_order_id: String,
        /// Expected entry price from signal
        expected_price: Price,
        /// Signal ID for idempotency tracking
        signal_id: uuid::Uuid,
    },

    /// Update the trailing stop price (favorable price movement)
    UpdateTrailingStop {
        /// Position to update
        position_id: PositionId,
        /// Previous stop price
        previous_stop: Price,
        /// New stop price
        new_stop: Price,
        /// Price that triggered the update
        trigger_price: Price,
    },

    /// Trigger position exit (trailing stop hit)
    TriggerExit {
        /// Position to exit
        position_id: PositionId,
        /// Reason for exit
        reason: ExitReason,
        /// Price that triggered the exit
        trigger_price: Price,
        /// Stop price that was hit
        stop_price: Price,
    },

    /// Place exit order (market order to close position)
    PlaceExitOrder {
        /// Position being exited
        position_id: PositionId,
        /// Query/risk cycle identifier proving governed execution.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        cycle_id: Option<uuid::Uuid>,
        /// Symbol to trade
        symbol: Symbol,
        /// Order side (opposite of position side)
        side: robson_domain::OrderSide,
        /// Quantity to exit
        quantity: Quantity,
        /// Reason for exit
        reason: ExitReason,
    },

    /// Place a reduce-only protective insurance stop on the exchange
    /// (ADR-0039).
    ///
    /// Emitted on entry fill (and on a trailing-stop advance when no prior
    /// stop exists). The stop price is always the position's current
    /// chart-derived trailing stop — never a percentage of entry.
    PlaceInsuranceStop {
        /// Position being protected
        position_id: PositionId,
        /// Symbol to trade
        symbol: Symbol,
        /// Close side for the position (Long → Sell, Short → Buy)
        side: robson_domain::OrderSide,
        /// Quantity to protect (the open position size)
        quantity: Quantity,
        /// Executable stop price (technical stop offset by the configured
        /// buffer; equals the technical stop when the buffer is zero)
        stop_price: Price,
    },

    /// Cancel-replace the protective insurance stop after a trailing advance
    /// (ADR-0039). The previous order is cancelled first; "unknown order"
    /// errors are tolerated (the stop may have just filled).
    ReplaceInsuranceStop {
        /// Position being protected
        position_id: PositionId,
        /// Symbol to trade
        symbol: Symbol,
        /// Close side for the position
        side: robson_domain::OrderSide,
        /// Quantity to protect
        quantity: Quantity,
        /// Exchange order id of the previous stop being replaced
        previous_order_id: String,
        /// New chart-derived trailing stop price
        new_stop_price: Price,
    },

    /// Cancel the protective insurance stop (ADR-0039).
    ///
    /// Inserted before `PlaceExitOrder` so a reduce-only stop and a market
    /// exit never coexist. "Unknown order" errors are tolerated.
    CancelInsuranceStop {
        /// Position being exited
        position_id: PositionId,
        /// Symbol to trade
        symbol: Symbol,
        /// Exchange order id of the stop to cancel
        order_id: String,
    },

    /// Emit domain event for audit/persistence
    EmitEvent(Event),
}

impl EngineAction {
    /// Return this action with order-placement audit events stamped with the
    /// execution cycle id.
    pub fn with_cycle_id(self, cycle_id: uuid::Uuid) -> Self {
        match self {
            EngineAction::EmitEvent(Event::EntryOrderPlaced {
                position_id,
                cycle_id: existing_cycle_id,
                order_id,
                expected_price,
                quantity,
                signal_id,
                timestamp,
            }) => EngineAction::EmitEvent(Event::EntryOrderPlaced {
                position_id,
                cycle_id: existing_cycle_id.or(Some(cycle_id)),
                order_id,
                expected_price,
                quantity,
                signal_id,
                timestamp,
            }),
            EngineAction::EmitEvent(Event::EntryOrderRequested {
                position_id,
                cycle_id: existing_cycle_id,
                order_id,
                client_order_id,
                expected_price,
                quantity,
                signal_id,
                timestamp,
            }) => EngineAction::EmitEvent(Event::EntryOrderRequested {
                position_id,
                cycle_id: existing_cycle_id.or(Some(cycle_id)),
                order_id,
                client_order_id,
                expected_price,
                quantity,
                signal_id,
                timestamp,
            }),
            EngineAction::PlaceEntryOrder {
                position_id,
                cycle_id: existing_cycle_id,
                symbol,
                side,
                quantity,
                order_id,
                client_order_id,
                expected_price,
                signal_id,
            } => EngineAction::PlaceEntryOrder {
                position_id,
                cycle_id: existing_cycle_id.or(Some(cycle_id)),
                symbol,
                side,
                quantity,
                order_id,
                client_order_id,
                expected_price,
                signal_id,
            },
            EngineAction::EmitEvent(Event::ExitOrderPlaced {
                position_id,
                cycle_id: existing_cycle_id,
                order_id,
                expected_price,
                quantity,
                exit_reason,
                timestamp,
            }) => EngineAction::EmitEvent(Event::ExitOrderPlaced {
                position_id,
                cycle_id: existing_cycle_id.or(Some(cycle_id)),
                order_id,
                expected_price,
                quantity,
                exit_reason,
                timestamp,
            }),
            EngineAction::PlaceExitOrder {
                position_id,
                cycle_id: existing_cycle_id,
                symbol,
                side,
                quantity,
                reason,
            } => EngineAction::PlaceExitOrder {
                position_id,
                cycle_id: existing_cycle_id.or(Some(cycle_id)),
                symbol,
                side,
                quantity,
                reason,
            },
            other => other,
        }
    }
}

